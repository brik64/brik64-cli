#!/usr/bin/env node
const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

const root = process.env.BRIK64_CLI_ROOT
  ? path.resolve(process.env.BRIK64_CLI_ROOT)
  : path.resolve(__dirname, '..');
const outDir = path.join(root, 'evidence', 'beta17-fixpoint');

function sha256Text(text) {
  return crypto.createHash('sha256').update(text).digest('hex');
}

function writeJson(file, object) {
  const body = `${JSON.stringify(object, null, 2)}\n`;
  fs.writeFileSync(file, body);
  return sha256Text(body);
}

function writeText(file, text) {
  fs.writeFileSync(file, text);
  return sha256Text(text);
}

function rel(file) {
  return path.relative(root, file);
}

function requireForce() {
  if (!fs.existsSync(outDir)) return;
  if (process.argv.includes('--force')) return;
  throw new Error(`${rel(outDir)} already exists; pass --force to replace template files`);
}

function templateBase(kind) {
  return {
    schemaVersion: `brik64.beta17_fixpoint.${kind}.v1`,
    version: '0.1.0-beta.17',
    status: 'TEMPLATE_NON_CLAIM',
    claimBoundary: {
      publicReleaseAllowed: false,
      definitiveFixpointAllowed: false,
      formalN5ClaimAllowed: false,
      universalCorrectnessClaimAllowed: false,
    },
  };
}

function main() {
  requireForce();
  fs.mkdirSync(outDir, { recursive: true });

  const written = [];
  function record(file, sha256) {
    written.push({ path: rel(file), sha256 });
  }

  record(
    path.join(outDir, 'canonical_motor_manifest.json'),
    writeJson(path.join(outDir, 'canonical_motor_manifest.json'), {
      ...templateBase('canonical_motor_manifest'),
      pcdBound: false,
      requiredReplacement: 'Replace with the canonical PCD/polymer manifest for the Beta17 motor.',
      requiredFields: [
        'pcdBound=true',
        'canonicalPcdRefs[]',
        'canonicalPolymerRefs[]',
        'inputSetSha256',
        'engineSemanticVersion',
      ],
    })
  );
  record(
    path.join(outDir, 'canonical_harness_manifest.json'),
    writeJson(path.join(outDir, 'canonical_harness_manifest.json'), {
      ...templateBase('canonical_harness_manifest'),
      pcdBound: false,
      requiredReplacement: 'Replace with the canonical PCD/polymer manifest for the Beta17 harness.',
      requiredFields: [
        'pcdBound=true',
        'harnessPcdRefs[]',
        'adversarialPcdRefs[]',
        'inputSetSha256',
      ],
    })
  );
  record(
    path.join(outDir, 'input_pcd_hashes.tsv'),
    writeText(
      path.join(outDir, 'input_pcd_hashes.tsv'),
      [
        '# path\tsha256',
        '# pcd/beta17/motor/<canonical-file>.pcd\t<sha256>',
        '# pcd/beta17/harness/<canonical-file>.pcd\t<sha256>',
        '',
      ].join('\n')
    )
  );
  record(
    path.join(outDir, 'stage1_artifact_manifest.json'),
    writeJson(path.join(outDir, 'stage1_artifact_manifest.json'), {
      ...templateBase('stage1_artifact_manifest'),
      generatedByL6PlusN5: false,
      requiredReplacement: 'Replace with the first artifact materialized by the authorized L6+N5 factory.',
      requiredFields: [
        'version=0.1.0-beta.17',
        'generatedByL6PlusN5=true',
        'l6plusSerial',
        'artifactPath',
        'artifactSha256',
        'inputSetSha256',
      ],
    })
  );
  record(
    path.join(outDir, 'stage2_regeneration_manifest.json'),
    writeJson(path.join(outDir, 'stage2_regeneration_manifest.json'), {
      ...templateBase('stage2_regeneration_manifest'),
      generatedByStage1: false,
      requiredReplacement: 'Replace with the artifact regenerated by the Stage1 artifact.',
      requiredFields: [
        'version=0.1.0-beta.17',
        'generatedByStage1=true',
        'stage1ArtifactSha256',
        'stage2ArtifactPath',
        'stage2ArtifactSha256',
      ],
    })
  );
  record(
    path.join(outDir, 'byte_identical_report.json'),
    writeJson(path.join(outDir, 'byte_identical_report.json'), {
      ...templateBase('byte_identical_report'),
      decision: 'TEMPLATE_NON_CLAIM',
      byteIdentical: false,
      requiredReplacement: 'Replace with a byte/hash-identical Stage1 vs Stage2 comparison report.',
    })
  );
  record(
    path.join(outDir, 'harness_report.json'),
    writeJson(path.join(outDir, 'harness_report.json'), {
      ...templateBase('harness_report'),
      decision: 'TEMPLATE_NON_CLAIM',
      adversarialCases: 0,
      requiredReplacement: 'Replace with a passing Beta17 harness report including at least three adversarial cases.',
    })
  );
  record(
    path.join(outDir, 'seal_report.json'),
    writeJson(path.join(outDir, 'seal_report.json'), {
      ...templateBase('seal_report'),
      decision: 'TEMPLATE_NON_CLAIM',
      sealed: false,
      requiredReplacement: 'Replace with a passing seal report binding PCD inputs, artifacts, package and release manifest.',
    })
  );
  record(
    path.join(outDir, 'public_surface_sync_report.json'),
    writeJson(path.join(outDir, 'public_surface_sync_report.json'), {
      ...templateBase('public_surface_sync_report'),
      decision: 'TEMPLATE_NON_CLAIM',
      synced: false,
      requiredReplacement: 'Replace after all public surfaces point to the exact Beta17 artifact and package.',
    })
  );
  record(
    path.join(outDir, 'external_audit_report.json'),
    writeJson(path.join(outDir, 'external_audit_report.json'), {
      ...templateBase('external_audit_report'),
      decision: 'TEMPLATE_NON_CLAIM',
      pass: false,
      requiredReplacement: 'Replace with the clean external audit result from public surfaces following docs/ops/BETA17_EXTERNAL_AUDIT_PROMPT.md.',
      requiredContract: {
        cleanPublicInstall: false,
        functionalTests: false,
        generatedCodeTests: false,
        adversarialTests: false,
        publicSurfaceScan: false,
        claimSafeScan: false,
        artifactRefs: {
          auditLog: false,
          generatedCodeQuality: false,
          adversarialResults: false,
          publicSurfaceScan: false,
          claimSafeScan: false,
        },
      },
    })
  );
  record(
    path.join(outDir, 'README.md'),
    writeText(
      path.join(outDir, 'README.md'),
      [
        '# Beta17 Fixpoint Evidence Pack',
        '',
        'This directory is the required evidence location for the Beta17 fixpoint lane.',
        '',
        'Files generated by `npm run beta17:fixpoint:evidence:init` are templates only.',
        'They are intentionally `TEMPLATE_NON_CLAIM` and must not satisfy release gates.',
        '',
        'Publication requires fresh replacement artifacts proving:',
        '',
        '- canonical PCD/polymer motor and harness manifests;',
        '- Stage1 artifact generated by the authorized L6+N5 factory;',
        '- Stage2 artifact regenerated by Stage1;',
        '- byte-identical Stage1/Stage2 comparison;',
        '- passing harness with adversarial coverage;',
        '- seal report;',
        '- public surface synchronization;',
        '- clean external audit following docs/ops/BETA17_EXTERNAL_AUDIT_PROMPT.md.',
        '',
      ].join('\n')
    )
  );

  const summary = {
    schemaVersion: 'brik64.beta17_fixpoint.evidence_pack_template_summary.v1',
    version: '0.1.0-beta.17',
    status: 'TEMPLATE_NON_CLAIM',
    generatedAt: new Date().toISOString(),
    claimBoundary: {
      publicReleaseAllowed: false,
      definitiveFixpointAllowed: false,
      formalN5ClaimAllowed: false,
      universalCorrectnessClaimAllowed: false,
    },
    written,
  };
  const summaryPath = path.join(outDir, 'template_summary.json');
  record(summaryPath, writeJson(summaryPath, summary));

  console.log(`BETA17_FIXPOINT_EVIDENCE_TEMPLATE_READY ${rel(outDir)}`);
  console.log(`files=${written.length}`);
}

try {
  main();
} catch (error) {
  console.error(error.message);
  process.exit(1);
}
